package com.project1.planner;

public class PlanDateVo {
	String planDateNum;
	int sno;
	String purchaseSerial;
	public String getPlanDateNum() {
		return planDateNum;
	}
	public void setPlanDateNum(String planDateNum) {
		this.planDateNum = planDateNum;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getPurchaseSerial() {
		return purchaseSerial;
	}
	public void setPurchaseSerial(String purchaseSerial) {
		this.purchaseSerial = purchaseSerial;
	}
}
