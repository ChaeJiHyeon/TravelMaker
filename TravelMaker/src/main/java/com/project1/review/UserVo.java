package com.project1.review;

public class UserVo {
	String email;
	String nickName;
	String userComment;
	String oriUserPhoto;
	String sysUserPhoto;
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getUserComment() {
		return userComment;
	}
	public void setUserComment(String userComment) {
		this.userComment = userComment;
	}
	public String getOriUserPhoto() {
		return oriUserPhoto;
	}
	public void setOriUserPhoto(String oriUserPhoto) {
		this.oriUserPhoto = oriUserPhoto;
	}
	public String getSysUserPhoto() {
		return sysUserPhoto;
	}
	public void setSysUserPhoto(String sysUserPhoto) {
		this.sysUserPhoto = sysUserPhoto;
	}

}
